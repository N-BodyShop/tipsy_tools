#include "siman.hpp"

namespace siman {

Metric::Metric() {
}

float Metric::operator()(const Particle &p1, const Particle &p2) const {
  return sqrt((p1.x-p2.x)*(p1.x-p2.x) + (p1.y-p2.y)*(p1.y-p2.y) + (p1.z-p2.z)*(p1.z-p2.z));
}


Metric2D::Metric2D() {
}

float Metric2D::operator()(const Particle &p1, const Particle &p2)  const {
  return sqrt((p1.x-p2.x)*(p1.x-p2.x) + (p1.y-p2.y)*(p1.y-p2.y));
}

}
